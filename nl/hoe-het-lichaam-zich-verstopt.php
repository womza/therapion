<p>
Het mechanisme van onze verschillende soorten waarneming is dus nooit het focuspunt van die waarneming.  
Volgens Drew Leder (Johnson, 2007) is het voor het goed functioneren zelfs nodig dat onze lichaamprocessen zich verbergen. 
</p>

<p>
We zijn ons bewust van het zien, niet van onze ogen en we ruiken niet ons neusweefsel of horen niet onze oren, zegt hij nog. Dit noemt Leder het ‘focal disappearance’-effect. 
</p>

<p>
Damasio (2000) beaamt dit mechanisme en zegt dat het een functie is van onze geest om te dienen als een soort scherm dat een deel van onze interne gewaarwordingen verbergt. ‘One of the things the screen hides most effectively is the body, our own body, by which I mean the ins of it, its interiors’, schrijft hij (Damasio, 2000, p. 28). 
</p>

<p>
Damasio zegt er ook nog bij dat dit mechanisme een evolutionair adaptatieve verklaring heeft omdat, in de meeste omstandigheden het veel voordeliger is om onze energie en bekwaamheden te gebruiken om de problemen in de buitenwereld te beschrijven en te begrijpen, zodat we ze kunnen oplossen.
</p>

<p>
Ook Merleau-Ponty schreef in de Phénoménologie de la perception (1945) over de ‘bestendigheid’ van het lichaam, waarmee hij duidde op het altijd aanwezig zijn.
</p>

<p>
<em>
Or la permanence du corps propre est d’un tout autre genre: il n’est pas à la limite d’une exploration indéfinie, il se refuse à l’exploration et se présente toujours à moi sous le même angle. Sa permanence n’est pas une permanence dans le monde mais une permanence de mon côté. (Merleau-Ponty, 1945, p. 106)
</em>
</p>

<p>
Merleau-Ponty sluit de paragraaf van het vorige citaat af met de volgende conclusie : 
</p>

<p>
<em>
(le) corps non plus comme objet du monde, mais comme moyen de notre communication avec lui, au monde non plus comme somme d’objets déterminés, mais comme horizon latent de notre expérience, présent sans cesse, lui aussi, avant toute pensée déterminante. (1945, p.109) 
</em>
</p>

