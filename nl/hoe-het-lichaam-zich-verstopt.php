						<h1>Hoe het lichaam zich verstopt</h1> 
						 
						 
<p>
Dat Descartes tot de conclusie kwam, dat een mens uit twee verschillende componenten bestaat, namelijk lichaam en geest, kunnen we hem toch wel vergeven. 
</p>

<p>
Deze dualistische visie wordt door onze dagelijkse ervaring bestendigd, omdat ons lichaam zich onttrekt aan ons bewustzijn en zichzelf verbergt, waardoor we een natuurlijke indruk krijgen van een zekere gespletenheid. 
</p>

<p>
Eén van de mechanismen die hiervoor verantwoordelijk is, heeft Polanyi (Johnson, 2007) de ‘van-naar’ perceptie genoemd. Bij het waarnemen zijn wij georiënteerd naar het ding dat wordt waargenomen. Dus de actie van het waarnemen wordt ervaren als een weggaan van het lichaam, naar buiten toe, wat in de fenomenologie de ‘intentionality of mind’ wordt genoemd. 
</p>

<p>
Polanyi verwoordde het als volgt :
</p>

<p>
<em>
Our body is the only assembly of things known almost exclusively by relying on our awareness of them for attending to something else. (Johnson, 2007, p. 4)
</em>
</p>
 
 
