<h3>
3. What is a booking form ? 
</h3>

<p>
A booking form is what the clients fills out when he or she makes the booking. It includes the client's contact details, and all information about the sessions that he or she wishes to have with you.
</p>

<p>
A booking form has capital letters in the title line. It looks like this: "PETE: E-MAIL: Winona Sample-Client: Arizona: 10:00 am"
</p>

<h3>
4. What is a confirmation (OK) message ? 
</h3>						 
					
<p>
A confirmation message is a proof that the session was paid for. It comes from our office to your e-mail, and you don't need to answer it.
</p>

<p>
A confirmation message has capital letters in the title, and it looks like this: "OK: PETE: E-MAIL: Winona Sample-Client: Arizona: 10:00 am"
</p>

<p>
As you can see, a booking form and the comfirmation message make a happy couple. Remember, you need to get both of them to be able to give the service.
</p>

 
