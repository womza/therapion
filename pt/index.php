<h3>
Tema do Mês - Ciúmes
</h3>

<p>
O Ciúme para a psicologia, é uma atitude inesperada quando nos sentimos
ameaçados por fatores reais ou imaginários, como o medo de perder o
objeto de desejo. Portanto, pode ocorrer em diversas áreas da vida, como
na família, no trabalho, no relacionamento afetivo, na amizade e assim
por diante. 
</p>

<p>
O Ciúme é uma emoção natural, mas pode partir de um contexto
simples como discussões, ataque de fúria e agressões violentas. Sabemos
que este sentimento está no contexto da vida humana e que se instala na
infância, pois é nesta fase que se percebe a reação e a disputa da
criança com os genitores devido a entrada de uma terceira pessoa na
relação, sendo que esse sentimento continua até a fase adulta. 
</p>

<p>
A questão é: Como reagir após o sentimento de ciúme? O mais saudável é que
predomine o pensamento bom sobre o sentimento ruim e uma boa reflexão
para entender que ter ciúme é uma reação natural, mas é uma emoção,
portanto, não é racional. Em análise final, temos que entender enquanto
adultos, que o outro é o nosso objeto de desejo, porém, não é o nosso
objeto de posse. 
</p>

<p>
O serviço do Therapion pode ajudá-lo a resolver sua
dificuldade em lidar com o ciúme de maneira prática e objetiva, através
da técnica de reflexão do real e do imaginário. 
</p>

			
